	MACRO CONVERT_BYTE b, bgclr, fgclr
m = %10000000
		WHILE m > 0
r = 0
			IF b & m
r = r | (fgclr << 4)
			ELSE
r = r | (bgclr << 4)
			ENDIF
m = m >> 1
			IF b & m
r = r | fgclr
			ELSE
r = r | bgclr
			ENDIF
m = m >> 1
			DEFB	r
		ENDW
	ENDM

