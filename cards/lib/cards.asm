CHAR_LINE_UP			EQU		0x04

FLAG_DEFAULT			EQU		0x00
FLAG_MIRROR_BYTE		EQU		(1 << MIRROR_BYTE)			; 1
FLAG_REVERSE_DATA		EQU		(1 << REVERSE_DATA)			; 2
FLAG_CHAR_REPEAT		EQU		(1 << CHAR_REPEAT)			; 4
FLAG_REVERSE_DF_ADDR	EQU		(1 << REVERSE_DF_ADDR)		; 8
FLAG_CHAR_LINE_UP		EQU		(1 << CHAR_LINE_UP)			; 16

drawCharacters        
	PUSH	BC          
	LD		B,C         
	PUSH	HL          
nxtCharacterCell
	PUSH	BC          
	PUSH	HL          
	LD		B,0x08       
nxtPixelLine
	LD		A,(DE)      
	BIT		MIRROR_BYTE,(IX+0x00)  
	JR		Z,byteNotMirrored     
	IFDEF	ZXN_CARDS_LIB
		MIRROR	A			; can be done in one instruction on the ZXN :)
	ELSE
		LD		C,A         
		LD		A,0x01       
nxtShift
		RR		C           
		RLA              
		JR		NC,nxtShift
	ENDIF
byteNotMirrored
	LD		(HL),A      
	BIT		REVERSE_DATA,(IX+0x00)  
	JR		Z,advanceCharacterData     
	DEC		DE          
	DEC		DE      
advanceCharacterData    
	INC		DE          
	INC		H           
	DJNZ	nxtPixelLine       
	BIT		CHAR_REPEAT,(IX+0x00)  
	JR		Z,noCharacterRepeat     
	LD		A,E         
	SUB		0x08         
	LD		E,A
noCharacterRepeat       
	POP		HL          
	INC		HL          
	BIT		REVERSE_DF_ADDR,(IX+0x00)  
	JR		Z,advanceScreenAddress     
	DEC		HL          
	DEC		HL
advanceScreenAddress          
	POP		BC          
	DJNZ	nxtCharacterCell       
	POP		HL          
	LD		BC,0x0020    
	BIT		CHAR_LINE_UP,(IX+0x00)  
	JR		Z,advanceCharacterLine     
	LD		BC,0xFFE0  
advanceCharacterLine  
	RRC		H           
	RRC		H           
	RRC		H           
	ADD		HL,BC       
	RLC		H           
	RLC		H           
	RLC		H           
	POP		BC          
	DJNZ	drawCharacters       ; next character line
	RET         
drawCornerCharacters     
	PUSH	BC          
	PUSH	BC          
	PUSH	DE          
	LD		C,0x00       
	CALL	offsetDFAddress       
	POP		DE          
	LD		BC,0x0101    
	LD		(IX+0x00),FLAG_DEFAULT
	CALL	drawCharacters		; draw top left character
	DEC		DE          
	POP		BC          
	LD		A,0x09       
	SUB		B           
	LD		B,A         
	LD		C,0x07       
	PUSH	DE          
	CALL	offsetDFAddress       
	POP		DE          
	LD		BC,0x0101    
	LD		(IX+0x00),FLAG_MIRROR_BYTE | FLAG_REVERSE_DATA	;0x03
	CALL	drawCharacters		; draw mirrored bottom right character
	POP		BC          
	INC		B           
	RET
	IFNDEF	ZXN_CARDS_LIB  
parameterOutOfRange            
		RST		0x08
		DEFB	0x09
		LD		DE,0x1FDB    
nxtPop
		POP		HL          
		AND		A           
		SBC		HL,DE       
		JR		NZ,nxtPop    
		PUSH	DE          
		LD		A,0x0F + 0x02	; +2 to allow print on bottom two lines
		SUB		(IY+0x31)    
		PUSH	AF          
		CALL	readParameter       
		POP		BC          
		CP		B           	; max y < 15
		JR		NC,parameterOutOfRange    
		PUSH	AF          
		CALL	readParameter       
		CP		0x19         	; max x < 25
		JR		NC,parameterOutOfRange    
		LD		C,A         
		POP		AF          
		LD		B,A         
		LD		HL,0x0800    
		CALL	calculateScreenAddress       
		LD		(cardTLDFAddress),HL  
		PUSH	DE          	; save ATTR address
		CALL	readParameter       
		CP		0x0E		; max card number check < 14
		JR		NC,parameterOutOfRange    
		PUSH	AF          
		CALL	readParameter       
		CP		0x05		; max suit check < 5
		JR		NC,parameterOutOfRange    
		LD		C,A         
		POP		AF          
		LD		B,A         
		POP		HL          	; restore ATTR address
	ENDIF
doDrawCard
	PUSH	BC          
	INC		C           
	DEC		C           	; ATTR	 FBPPPIII
	LD		A,0x39       	;	%00111001 - Blue on White
	JR		Z,drawCardBackground     	; branch if suit 0 (back of card)
	BIT		0,C         
	JR		NZ,redCard    
	DEC		A          	;  Now Black on White
	DEC		A 				;  Now White on White
redCard         
	INC		A    			;  Now Red on White or Black on White
drawCardBackground       
	LD		DE,0x0018		; colour card background
	LD		C,0x0A    
nxtAttrLine   
	LD		B,0x08       
nxtAttrCell
	LD		(HL),A      
	INC		HL          
	DJNZ	nxtAttrCell       
	ADD		HL,DE       
	DEC		C           
	JR		NZ,nxtAttrLine    
	LD		HL,(cardTLDFAddress)  
	LD		BC,0x0A08    
	LD		DE,0x3D00    	; space character data in ROM
	LD		A,FLAG_CHAR_REPEAT
	LD		IX,flags
	LD		(IX+0x00),A
	CALL	drawCharacters
	LD		BC,0x0101
	CALL	offsetDFAddress
	POP		DE
	INC		E
	DEC		E
	JR		NZ,nonZeroSuit
	LD		BC,0x0806	; fill in card back
	LD		DE,cardBackData
	JP		drawCharacters
nonZeroSuit
	LD		A,D
	AND		A
	JR		NZ,nonZeroValue
	LD		BC,0x0806
	LD		DE,jokerCardData
	LD		(IX+0x00),A	; FLAG_DEFAULT
	CALL	drawCharacters
	LD		B,0x00
	LD		DE,0x3E50		; "J"
	CALL	drawCornerCharacters
	LD		E,0x78			; "O"
	CALL	drawCornerCharacters
	LD		E,0x58			; "K"
	CALL	drawCornerCharacters
	LD		E,0x28			; "E"
	CALL	drawCornerCharacters
	LD		E,0x90			; "R"
	JP		drawCornerCharacters
nonZeroValue
	PUSH	HL
	PUSH	DE
	ADD		A,A
	LD		L,A
	LD		H,0x00
	LD		DE,characterDataLookup - 2
	ADD		HL,DE
	LD		E,(HL)
	INC		HL
	LD		D,(HL)
	LD		B,0x00
	CALL	drawCornerCharacters
	POP		DE
	PUSH	DE
	LD		A,E
	ADD		A,A
	ADD		A,A
	ADD		A,A
	ADD		A,(low smallSuitCharData) - 8 ;0xB8		
	LD		E,A
	LD		D,high smallSuitCharData
	LD		B,0x01
	CALL	drawCornerCharacters
	LD		HL,0x0104
	POP		DE
	AND		A
	SBC		HL,DE
	JR		NZ,notAceOfSpades
	LD		BC,0x0803
	LD		DE,aceOfSpadesCardData
	POP		HL
	LD		(IX+0x00),FLAG_DEFAULT
	CALL	drawCharacters
	LD		BC,0x0106
	CALL	offsetDFAddress
	LD		BC,0x0803
	LD		DE,aceOfSpadesCardData
	LD		(IX+0x00), FLAG_MIRROR_BYTE | FLAG_REVERSE_DF_ADDR ;0x09
	CALL	drawCharacters
	LD		BC,0x0404
	CALL	offsetDFAddress
	LD		BC,0x0101
	LD		DE,aceOfSpadesPatchData
	LD		(IX+0x00),FLAG_DEFAULT
	CALL	drawCharacters
	RET
notAceOfSpades
	LD		A,D
	CP		0x0B
	JR		C,notPictureCard		; branch if not a picture card
	ADD		A,E
	ADD		A,E
	ADD		A,E
	LD		HL,0x00C0		; 192 bytes per picture half (6 x 4 x 8)
	LD		D,H
	LD		E,A
	CALL	0x30A9		; HL = HL * DE
	LD		DE,pictureCardData - 0x0A80	; calculate picture card bitmap data address
	ADD		HL,DE
	EX		DE,HL
	LD		BC,0x0406
	POP		HL
	LD		(IX+0x00),FLAG_DEFAULT
	CALL	drawCharacters
	LD		BC,0x0501
	PUSH	DE
	CALL	offsetDFAddress
	POP		DE
	DEC		DE
	LD		BC,0x0406
	LD		(IX+0x00),FLAG_MIRROR_BYTE | FLAG_REVERSE_DATA ;0x03
	JP		drawCharacters
notPictureCard
	POP		HL
	LD		A,E
	ADD		A,A
	ADD		A,A
	ADD		A,A
	ADD		A,A
	ADD		A,A
	ADD		A,(largeSuitCharData & 0xff) - 0x20
	LD		L,A
	LD		H,high largeSuitCharData
	PUSH	HL							; address of large symbol bitmap data
	LD		HL,symbolLayoutTable		; address of symbol location data
	LD		B,0x00
	LD		C,D						; card number 1=ace,2=2,11=Jack, etc
	JR		calcLayoutAddr
nxtLayoutAddr
	ADD		HL,BC
calcLayoutAddr
	DEC		C
	JR		NZ,nxtLayoutAddr
	LD		E,0x00						; HL = address of symbol position data to use
drawNxtSymbol
	LD		A,(HL)		
	CP		0x50						; is symbol mirrored
	JR		C,notMirrored
	INC		E
	DEC		E
	JR		NZ,notMirrored					; is E=0 ?
	EX		(SP),HL					; yes
	LD		BC,0x001F
	ADD		HL,BC						; position symbol data at the end
	EX		(SP),HL
	LD		E,FLAG_MIRROR_BYTE | FLAG_REVERSE_DATA	; 0x03
notMirrored
	RRA
	RRA
	RRA
	RRA
	AND		0x0F		; X offset
	LD		B,A
	LD		A,(HL)
	AND		0x0F		; y offset
	LD		C,A
	INC		HL
	LD		(IX+0x00),E		; flags 0=normal, 3=mirrored
	EX		(SP),HL
	PUSH	DE
	PUSH	HL
	CALL	offsetDFAddress
	POP		DE
	PUSH	DE
	LD		BC,0x0202		; symbol is 2x2 chars
	CALL	drawCharacters
	POP		BC
	POP		DE
	POP		HL
	PUSH	BC
	DEC		D
	JR		NZ,drawNxtSymbol
	POP		HL
	RET
